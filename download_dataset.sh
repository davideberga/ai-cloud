#!/bin/bash

DATASET_NAME="amazon"
DATASET_URL="https://snap.stanford.edu/data/bigdata/communities/com-amazon.ungraph.txt.gz"
OUTPUT_DIR="testgraphs"
mkdir -p "$OUTPUT_DIR"
echo "Downloading dataset from $DATASET_URL..."
curl -L "$DATASET_URL" -o "${OUTPUT_DIR}/${DATASET_NAME}.txt.gz"
echo "Extracting file..."
gunzip -f "${OUTPUT_DIR}/${DATASET_NAME}.txt.gz"
echo "Dataset saved in: ${OUTPUT_DIR}/${DATASET_NAME}.txt"

